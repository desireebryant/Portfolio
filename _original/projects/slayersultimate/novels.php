			<p>Published by Fujimi Shobo, (AUS)Madman Entertainment, (US)Tokyopop (1991-2008)
			Novels 1 to 8 contain five conclusive episodes, previously published in Dragon Magazine, while novels 9 to 30 contain three 2-chapters episodes. Besides, except novel 8, all other novels have another conclusive episode, written specifically for each novel. This last episode is subtitled as Slayers Excellent (novels 1 to 7) and as Slayers Superior (novels 9 to 30).</p>
			
